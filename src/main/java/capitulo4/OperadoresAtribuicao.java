package capitulo4;

public class OperadoresAtribuicao {
    public static void main(String[] args) {

        Double numero = 7.00;

        numero+=4;
        System.out.println(numero);

        numero-=4;
        System.out.println(numero);

        numero/=2;
        System.out.println(numero);

        numero%=2;
        System.out.println(numero);

        numero*=2;
        System.out.println(numero);
    }
}
