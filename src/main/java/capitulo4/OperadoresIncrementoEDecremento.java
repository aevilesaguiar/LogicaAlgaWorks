package capitulo4;

public class OperadoresIncrementoEDecremento {
    public static void main(String[] args) {

        Integer numero=10;

        ++numero;

        System.out.println(numero);

        ++numero;

        System.out.println(numero);

        numero++;

        System.out.println(numero);


        numero--;

        System.out.println(numero);

        --numero;

        System.out.println(numero);


    }
}
